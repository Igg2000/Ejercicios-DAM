    private void botonPlantarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPlantarseActionPerformed
        // TODO add your handling code here:
        a.getJ().setPlantado(true);
        JOptionPane.showMessageDialog(null, "Se ha plantado "+ a.getJ()+" con una cantidad de puntos de "
                                        + a.getJ().sumarPuntos());
        
        if (a.getJ1().isPlantado() && a.getJ2().isPlantado()){
        JOptionPane.showMessageDialog(null,"La partida ha finalizado");
        this.setVisible(false);
        
        VentanaResultados v = new VentanaResultados(a);
        v.setVisible(true);
        v.setResizable(false);
        v.setLocationRelativeTo(null);
        v.setSize(703, 454);
        
        this.dispose();
        }
        else
        cambiarTurno();
    }//GEN-LAST:event_botonPlantarseActionPerformed

    private void botonRobarCartaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRobarCartaActionPerformed
        // TODO add your handling code here:
        a.getJ().robarCarta(a.getBaraja());
        
        JOptionPane.showMessageDialog(null,"Has robado la siguiente carta: "+a.getJ().getManoJugador().getLast());
        
        
        inicializarTurno();
        
    }//GEN-LAST:event_botonRobarCartaActionPerformed

    private void cambiarTurno(){
        
        a.setEsTurnoDeJug1(! a.GetEsTurnoJ1());
        
        inicializarTurno();
    }
    
    
    private void inicializarTurno(){
        
        
        cadManoJugador="Cartas en la mano de "+ a.getJ().getNombre() +": \n ______________________ \n";
        tituloTurnoDelJugador.setText("Turno de "+ a.getJ().getNombre());
        verMano.setText(cadManoJugador + a.getJ().verMano());
    }
