    private void agregarEnumsAlComboBox(Enum[] values, JComboBox<String> comboBox) {
        
        comboBox.removeAllItems();
        
        //Convierto el array de enumerados con todos los valores, las paso a un array de Strings, y las a√±ado

        String[] lista = new String[values.length];
       
        for (int i = 0; i < values.length; i++) {
            lista[i]=values[i].name();
            comboBox.addItem(lista[i]);
        }
        
    }
