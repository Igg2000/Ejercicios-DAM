    private void inicializarPartida() {
        
        a.setEsTurnoDeJug1(a.empezarJuego());
        a.rellenarBaraja();
        a.getJ1().getManoJugador().clear();
        a.getJ2().getManoJugador().clear();
        a.getJ1().setPlantado(false);
        a.getJ2().setPlantado(false);
        inicializarTurno();    
    }
