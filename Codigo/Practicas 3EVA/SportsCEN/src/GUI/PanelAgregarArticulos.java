    private void bAgregarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarArticuloActionPerformed
        // genero un articulos con los datos dados por el usuario
        Marcas marca = null;
        //guardo la marca que he seleccionado
        for (int i = 0; i < Marcas.values().length; i++) {
            if(cbMarca.getSelectedItem()==Marcas.values()[i].name())
                marca=Marcas.values()[i];
        }
        Prendas prenda= null;
        //guardo la prenda que he seleccionado
        for (int i = 0; i < Prendas.values().length; i++) {
            if(cbPrenda.getSelectedItem()==Prendas.values()[i].name())
                prenda=Prendas.values()[i];
        }
        
        String modelo=tModelo.getText();
        
        Gerente g= (Gerente) pt;
        Articulo articulo;
       //si la longitud del modelo es menor que 3, se crea con un modelo basico, si no con el que se le de
        if (modelo.length()<3)
            articulo = new Articulo(prenda,marca);
        else
            articulo = new Articulo(prenda,marca,modelo);
        
        t.getArticulosTienda().add(articulo);
        
        JOptionPane.showMessageDialog(this, "Articulo agregado con exito");
        
        
    }//GEN-LAST:event_bAgregarArticuloActionPerformed

